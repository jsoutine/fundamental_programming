package com.company.l04_control_flow;

public class Task9 {
    public static void main(String[] args) {

        for (int i = 1; i < 100; i ++){
            System.out.println(i);
        }

        int x = 100;

        while ( x > 0){
            System.out.println(x);
            x --;
        }
    }
}
