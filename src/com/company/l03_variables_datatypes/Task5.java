// Task 5
// Prints out a bunch of variables.

package com.company.l03_variables_datatypes;

public class Task5 {
    public static void main (String[] args) {

        byte myByte = 5;
        short myShort = 10;
        int myInt = 100;
        long myLong = 1000;
        float myFloat = 3.14f;
        double myDouble = 123.34;
        boolean myBoolean = false;
        char myChar = 'A';

        System.out.println(myByte + "\n" + myShort + "\n" + myInt + "\n" + myLong + "\n"
        + myFloat + "\n" + myDouble + "\n" + myBoolean + "\n" + myChar);

    }
}
