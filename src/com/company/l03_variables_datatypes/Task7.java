// Task 7
// Incrementally increases 0

package com.company.l03_variables_datatypes;

public class Task7 {

    public static void main (String[] args){

        int value = 0;
        value = value + 1;
        System.out.println(value);
        value++;
        System.out.println(value);
        value += 1;
        System.out.println(value);
    }
}
